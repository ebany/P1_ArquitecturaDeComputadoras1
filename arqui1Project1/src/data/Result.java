package data;

/**
 *
 * @author edwin
 */
public class Result {
    private String name;
    private Integer points;

    public Result(String name, Integer points) {
        this.name = name;
        this.points = points;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Integer getPoints() {
        return points;
    }

    public void setPoints(Integer points) {
        this.points = points;
    }  
    
}
