    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO init play 
        Boolean facil   = this.jRadioButton1.isSelected();
        Boolean medio   = this.jRadioButton2.isSelected();
        Boolean dificil = this.jRadioButton3.isSelected();
        Integer numberSong = this.jList1.getSelectedIndex();
        
        if (numberSong >= 0 && (facil || medio || dificil)){
            Song song = Home.songs.get(numberSong);
            String nameSong = song.getNameSong();
            //Playing.mainPlaying(nameSong, numberSong, this); 
            //this.notificar();
            Integer difficulty = getDifficulty();
            managerGame= new ManagerGame(difficulty,song,arduino);
            managerGame.init();
        }else {
            this.showMessage("Debe seleccionar una cancion y un nivel de dificultad", "ERROR", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

