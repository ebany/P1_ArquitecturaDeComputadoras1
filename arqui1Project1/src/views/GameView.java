    private void addSong(){
        Integer numSongs = Home.songs.size() - 1;
        Song song = Home.songs.get(numSongs);
        DefaultListModel dlm = new DefaultListModel();
        ListModel<String> tmp = this.jList1.getModel();      
        for(int i=0; i < tmp.getSize(); i++){
            dlm.addElement(tmp.getElementAt(i));
        }     
        dlm.addElement(song.getNameSong());
        this.jList1.setModel(dlm);     
    }
    
